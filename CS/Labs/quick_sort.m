function y = quick_sort(x)
    % QUICK_SORT Рекурсивная реализация быстрой сортировки (Quick Sort).
    n = length(x);              % Определяем размер входного массива
    
    % Базовый случай: если в массиве 0 или 1 элемент, он уже отсортирован
    if n < 2 y = x; return
    end
    
    % Выбираем опорный элемент (pivot): здесь — последний элемент вектора
    pivot = x(n);
    
    % Инициализируем два пустых подмассива для элементов < pivot и >= pivot
    x1 = [];  % для элементов меньше pivot
    x2 = [];  % для элементов >= pivot
    
    % Разбиение: перебираем все элементы, кроме последнего (pivot)
    for i = 1:n-1
        if x(i) < pivot
            x1 = [x1, x(i)];  % добавляем в «левый» подмассив
        else
            x2 = [x2, x(i)];  % добавляем в «правый» подмассив
        end
    end
    
    % Рекурсивно сортируем левый и правый подмассивы
    % Объединяем: отсортированные меньшие, затем pivot, затем отсортированные большие
    y = [quick_sort(x1), pivot, quick_sort(x2)];
end