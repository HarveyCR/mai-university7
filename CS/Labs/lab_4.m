% Параметры
a = 1; 
b = 2; 
c = 1.5;
h = 5;

% Сетка координат
t = linspace(0, 2*pi, 100); % Угловые значения
z = linspace(-h, h, 100);   % Высота по оси z
[T, Z] = meshgrid(t, z);

% Уравнение конуса
X = a * Z .* cos(T); % x = a*z*cos(t)
Y = b * Z .* sin(T); % y = b*z*sin(t)
Z = c * Z;           % Масштабирование по оси z

% Построение поверхности
figure;
surf(X, Y, Z, Y); % Ориентация цветов по оси y
colormap(jet);    % Цветовая палитра
shading interp;   % Интерполяция цвета

% Настройки отображения
axis equal;       % Одинаковые масштабы
xlabel('X');
ylabel('Y');
zlabel('Z');
title('Конус второго порядка');
grid on;
% Настройка положения камеры
view(45, 30); % Азимут 45°, высота 30°
